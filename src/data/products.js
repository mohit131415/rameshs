// Helper functions for working with products
export function getProductById(id) {
  return products.find((product) => product.id === Number(id)) || null
}

export function getProductBySlug(slug) {
  return products.find((product) => product.slug === slug) || null
}

export function getProductsByCategory(category) {
  return category ? products.filter((product) => product.category === category) : products
}

export function getRelatedProducts(productId, limit = 4) {
  const currentProduct = getProductById(productId)
  if (!currentProduct) return []

  return products
    .filter(
      (product) =>
        product.id !== currentProduct.id &&
        (product.category === currentProduct.category || product.tags.some((tag) => currentProduct.tags.includes(tag))),
    )
    .slice(0, limit)
}

export function getBestsellerProducts(limit = 4) {
  return products.filter((product) => product.isBestseller || product.tags.includes("bestseller")).slice(0, limit)
}

export function getNewProducts(limit = 4) {
  return products.filter((product) => product.isNew || product.tags.includes("new")).slice(0, limit)
}

export function getFeaturedProducts(limit = 4) {
  return products.filter((product) => product.featured).slice(0, limit)
}

export function searchProducts(query) {
  const searchTerm = query.toLowerCase()
  return products.filter(
    (product) =>
      product.name.toLowerCase().includes(searchTerm) ||
      product.description.toLowerCase().includes(searchTerm) ||
      product.category.toLowerCase().includes(searchTerm) ||
      product.tags.some((tag) => tag.toLowerCase().includes(searchTerm)),
  )
}

